                    <img class="img-fluid w-100 rounded mb-5" src="img/ndole1.jpg" alt="">
                    <h1 class="text-uppercase mb-4">Ndolé</h1>
                    <p>Ndole, an aromatic Cameroonian spinach stew made of spinach and bitter leaves
                        flavored with garlic, crayfish and fortified with shrimp and beef. Comfort food 
                        at its best.</p>
                    <p>It's a dish that portrays and celebrates the country's willingness to share and 
                        accept their diversities in a way that unites them. That is what makes Cameroonian 
                        dishes so special. With just a bite, Ndole has a creamy peanut flavor with a mild 
                        residual bitter taste that comes from the bitter leaves</p>
                        <p>Let's see how Ndolé is cooked</p>
                    <p><strong>Ingredients</strong></p>
                    <p>2 Tablespoons Maggi ( Bouillon)</p>
                    <P>01 large Onion ( sliced)</P> 
                    <p></p>01 pound (about 2 cups )Groundnuts/Peanuts(Skinless)
                    <p>1/2 pound shrimp</p>
                    <p>1/2 pound stockfish</p>
                    <p>1/2- 1 pound Stew beef (cut in chunks)</p>
                    <p>4-5 garlic</p> 
                    <p>3/4 cup Crayfish (ground)</p>
                    <p>3/4 -1 pound Washed bitterleaves</p> 
                    <p>2-3 cups oil</p>
                    <p><strong>Recipe</strong></p>
                    <p>In a large pan season meat with salt, maggi and onions and boil until tender 
                        depending on the choice of meat. Meanwhile, boil stock fish with salt and water; 
                        add it to the boiled meat. You should have about 3 cups of stock from the meat and 
                        stock fish. Reserve the rest or freeze it. Boil peanuts for about 10 minutes in a sauce pan. 
                        Let it cool and blend/pulse in a food processor or blender into a fine consistency use water 
                        to facilitate the blending .Add to the mixture of beef and stockfish.</p>
                    <p>Blend one onion and garlic into a fine paste and add to the mixture of peanuts and meat. 
                        Pour in the crayfish and let it simmer for 10 minutes stirring frequently to prevent burns. 
                        Season with salt and Maggi. You might have to add more later.
                        Add the bitter leaves or spinach to the pot. Stir and simmer for several minutes more</p>
                    <p>While the pot of ndole is simmering, heat oil in a fry pan or, preferably a cast iron. 
                        Add the shrimp, stirring constantly until they just turn pink. Slice and add the remaining 
                        onions stir for a few more minutes. 
                        Finally incorporate the mixture of shrimp, onions and oil into the pot of Ndole. Stir for a 
                        few minutes and serve hot with plantains (ripe or unripe, fried), miondo/bobolo, cassava, and a host of others. <strong>Enjoy your meal.</strong></p>
